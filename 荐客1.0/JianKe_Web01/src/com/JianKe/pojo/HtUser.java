package com.JianKe.pojo;

public class HtUser {
	private  int hid;
	private String hname;
	private String hpwd;
	
	public HtUser(){}

	public HtUser(String hname, String hpwd) {
		super();
		this.hname = hname;
		this.hpwd = hpwd;
	}
	
	public HtUser(int hid, String hname, String hpwd) {
		super();
		this.hid = hid;
		this.hname = hname;
		this.hpwd = hpwd;
	}

	public int getHid() {
		return hid;
	}

	public void setHid(int hid) {
		this.hid = hid;
	}

	public String getHname() {
		return hname;
	}

	public void setHname(String hname) {
		this.hname = hname;
	}

	public String getHpwd() {
		return hpwd;
	}

	public void setHpwd(String hpwd) {
		this.hpwd = hpwd;
	}
	
	
}
