package com.JianKe.pojo;

import java.util.Set;

public class Goods {
	private int gid;
	private String gname;
	private float price;
	private String images;
	private String introduce;
	private int istesecai;
	private Shop shop;
	private Set<Goods_Category> goods_Categories;
	

	public Goods(){}
	
	public Shop getShop() {
		return shop;
	}
	
	public void setShop(Shop shop) {
		this.shop = shop;
	}
	

	public Set<Goods_Category> getGoods_Categories() {
		return goods_Categories;
	}

	public void setGoods_Categories(Set<Goods_Category> goods_Categories) {
		this.goods_Categories = goods_Categories;
	}

	public int getGid() {
		return gid;
	}
	
	public void setGid(int gid) {
		this.gid = gid;
	}
	
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public String getImages() {
		return images;
	}
	public void setImages(String images) {
		this.images = images;
	}
	public String getIntroduce() {
		return introduce;
	}
	public void setIntroduce(String introduce) {
		this.introduce = introduce;
	}

	public int getIstesecai() {
		return istesecai;
	}

	public void setIstesecai(int istesecai) {
		this.istesecai = istesecai;
	}
	
	
}
